// Capacitor's `sync` fires `capacitor:sync:after` once per platform. Gate so
// each invocation only touches files for its own platform. When unset (manual
// run), apply everything.
const platform = (process.env.CAPACITOR_PLATFORM_NAME || '').toLowerCase();
if (platform === 'web') {
  process.exit(0);
}
const runAndroid = !platform || platform === 'android';
const runIos = !platform || platform === 'ios';

const projectRoot = process.cwd();
const packageJsonPath = path.join(projectRoot, 'package.json');

if (!fs.existsSync(packageJsonPath)) {
  console.log(`${TAG} package.json not found. Skipping mediation injection.`);
  process.exit(0);
}

const packageJson = JSON.parse(fs.readFileSync(packageJsonPath, 'utf8'));
const config = packageJson.levelplay || {};
const requested = Array.isArray(config.networks) ? config.networks : [];

// Consent provider — 'inmobi' (default, IAB TCF v2.2 compliant) or 'custom'
// (plugin's built-in modal, non-TCF). When inmobi, the plugin bundles the
// InMobi Choice CMP SDK and the host app supplies a `pCode` from the InMobi
// Choice portal.
const consentProvider = (config.consentProvider || 'inmobi').toLowerCase();
if (!['inmobi', 'custom'].includes(consentProvider)) {
  console.log(`${TAG} ⚠️  Unknown consentProvider "${config.consentProvider}". Falling back to "inmobi".`);
}
const useInMobi = consentProvider === 'inmobi';
const inmobiCfg = config.inmobi || {};
if (useInMobi && !inmobiCfg.pCode) {
  console.log(
    `${TAG} ⚠️  consentProvider="inmobi" but no levelplay.inmobi.pCode in package.json. ` +
    `The CMP will not initialize. Get a pCode at https://choice.inmobi.com/.`,
  );
}

const networks = [];
for (const name of requested) {
  const key = String(name).toLowerCase();
  if (NETWORK_REGISTRY[key]) {
    networks.push({ key, ...NETWORK_REGISTRY[key] });
  } else {
    console.log(`${TAG} ⚠️  Unknown network "${name}". Supported: ${Object.keys(NETWORK_REGISTRY).join(', ')}`);
  }
}

if (networks.length === 0 && !useInMobi) {
  console.log(`${TAG} No networks and no CMP to wire. Nothing to inject.`);
  process.exit(0);
}

// InMobi POM declares zero transitive deps. Full list per official guide:
// https://support.inmobi.com/choice/getting-started-cmp/implementing-cmp-via-code/mobile-app/android-app-implementation-sdk
const INMOBI_ANDROID_COORDS = [
  'com.inmobi:inmobicmp:2.4.2',
  'com.google.android.material:material:1.12.0',
  'com.google.code.gson:gson:2.10.1',
  'com.iabgpp:iabgpp-encoder:3.2.3',
  'androidx.preference:preference:1.2.1',
  'androidx.constraintlayout:constraintlayout:2.1.4',
  'androidx.recyclerview:recyclerview:1.3.2',
  'androidx.cardview:cardview:1.0.0',
  'androidx.lifecycle:lifecycle-runtime-ktx:2.8.7',
];
const INMOBI_IOS_POD = 'InMobiCMP';

/** Replaces the region between BEGIN/END markers, or returns null if absent. */
function replaceMarkedRegion(content, beginComment, endComment, body) {
  const re = new RegExp(`${escapeRe(beginComment)}[\\s\\S]*?${escapeRe(endComment)}`);
  if (re.test(content)) {
    return content.replace(re, body);
  }
  return null;
}

function escapeRe(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

// ==========================================
// 1. ANDROID — android/app/build.gradle
// ==========================================
const gradlePath = path.join(projectRoot, 'android', 'app', 'build.gradle');
if (!runAndroid) {
  // skip — different platform pass
} else if (fs.existsSync(gradlePath)) {
  let gradle = fs.readFileSync(gradlePath, 'utf8');

  const coords = [];
  networks.forEach((n) => {
    coords.push(n.android);
    if (Array.isArray(n.androidSdks)) coords.push(...n.androidSdks);
  });
  if (useInMobi) coords.push(...INMOBI_ANDROID_COORDS);
  const lines = coords.map((c) => `    implementation '${c}'`).join('\n');
  const block = `// ${BEGIN}\ndependencies {\n${lines}\n}\n// ${END}`;

  const replaced = replaceMarkedRegion(gradle, `// ${BEGIN}`, `// ${END}`, block);
  if (replaced !== null) {
    gradle = replaced;
    console.log(`${TAG} ✅ Updated Android mediation adapters in android/app/build.gradle`);
  } else {
    gradle = `${gradle.trimEnd()}\n\n${block}\n`;
    console.log(`${TAG} ✅ Injected Android mediation adapters into android/app/build.gradle`);
  }
  fs.writeFileSync(gradlePath, gradle, 'utf8');
} else if (fs.existsSync(`${gradlePath}.kts`)) {
  // Kotlin-DSL build scripts use a different dependency syntax — don't risk a
  // malformed inject; tell the developer what to add by hand.
  const ktsCoords = [];
  networks.forEach((n) => {
    ktsCoords.push(n.android);
    if (Array.isArray(n.androidSdks)) ktsCoords.push(...n.androidSdks);
  });
  if (useInMobi) ktsCoords.push(...INMOBI_ANDROID_COORDS);
  const ktsLines = ktsCoords.map((c) => `    implementation("${c}")`).join('\n');
  console.log(
    `${TAG} ⚠️  Found build.gradle.kts (Kotlin DSL). Add these to the dependencies block manually:\n${ktsLines}`,
  );
} else {
  console.log(`${TAG} ℹ️  Android platform not found. Skipping Android adapter injection.`);
}

// ==========================================
// 2. IOS — ios/App/Podfile
// ==========================================
const podfilePath = path.join(projectRoot, 'ios', 'App', 'Podfile');
if (!runIos) {
  // skip — different platform pass
} else if (fs.existsSync(podfilePath)) {
  let podfile = fs.readFileSync(podfilePath, 'utf8');

  const podCoords = networks.map((n) => `  pod '${n.ios}'`);
  // InMobiCMP is a dependency of the plugin podspec — no need to inject here.
  const pods = podCoords.join('\n');
  const block = `  # ${BEGIN}\n${pods}\n  # ${END}`;

  const replaced = replaceMarkedRegion(podfile, `# ${BEGIN}`, `# ${END}`, block.trim());
  if (replaced !== null) {
    podfile = replaced;
    console.log(`${TAG} ✅ Updated iOS mediation adapter pods in Podfile`);
  } else {
    // Insert inside the `target 'App' do` block, before its `end`.
    const targetRe = /(target ['"]App['"] do[\s\S]*?)(\n\s*end)/;
    if (targetRe.test(podfile)) {
      podfile = podfile.replace(targetRe, `$1\n${block}$2`);
      console.log(`${TAG} ✅ Injected iOS mediation adapter pods into Podfile`);
    } else {
      console.log(`${TAG} ⚠️  Could not locate the "App" target in Podfile. Add these pods manually:\n${pods}`);
    }
  }
  // IronSourceSDK ships as a static xcframework. Capacitor's default
  // `use_frameworks!` (dynamic) rejects transitive static binaries.
  // Patch to `:linkage => :static` if not already set.
  if (/use_frameworks!\s*$/.test(podfile) && !/use_frameworks!\s+:linkage\s*=>/.test(podfile)) {
    podfile = podfile.replace(/use_frameworks!\s*$/m, "use_frameworks! :linkage => :static");
    console.log(`${TAG} ✅ Patched Podfile: use_frameworks! → use_frameworks! :linkage => :static`);
  }

  fs.writeFileSync(podfilePath, podfile, 'utf8');
} else {
  console.log(`${TAG} ℹ️  iOS platform not found. Skipping Podfile injection.`);
}

// ==========================================
// 3. IOS — Info.plist (ATT description + SKAdNetwork IDs)
// ==========================================
const plistPath = path.join(projectRoot, 'ios', 'App', 'App', 'Info.plist');
if (!runIos) {
  // skip — different platform pass
} else if (fs.existsSync(plistPath)) {
  const raw = fs.readFileSync(plistPath, 'utf8');
  const parsed = plistLib.parse(raw);
  const notes = [];

  // 3a. App Tracking Transparency usage description — insert only when
  // missing. The wording often goes through legal/localization review;
  // never overwrite it.
  if (!parsed.NSUserTrackingUsageDescription) {
    parsed.NSUserTrackingUsageDescription =
      config.userTrackingDescription ||
      'This identifier will be used to deliver personalized ads to you.';
    notes.push('added NSUserTrackingUsageDescription');
  }

  // 3b. SKAdNetwork IDs — merge with whatever is already there. Other SDKs
  // (AdMob, Firebase, RevenueCat) register their own IDs in the same array;
  // a replace would silently strip them.
  const ourIds = new Set(['su67r6k2v3.skadnetwork']); // IronSource
  networks.forEach((n) => n.skAdNetworkIds.forEach((id) => ourIds.add(id)));

  const items = Array.isArray(parsed.SKAdNetworkItems) ? parsed.SKAdNetworkItems : [];
  const existing = new Set(items.map((i) => i?.SKAdNetworkIdentifier).filter(Boolean));
  const missing = [...ourIds].filter((id) => !existing.has(id));
  if (missing.length > 0) {
    parsed.SKAdNetworkItems = [...items, ...missing.map((id) => ({ SKAdNetworkIdentifier: id }))];
    notes.push(`added ${missing.length} SKAdNetwork ID${missing.length === 1 ? '' : 's'}`);
  }

  // 3c. CMP provider selection — plugin reads these at runtime to decide
  // which ConsentProvider to instantiate.
  parsed.LevelPlayCMPProvider = useInMobi ? 'inmobi' : 'custom';
  if (useInMobi) {
    if (inmobiCfg.pCode) parsed.LevelPlayInMobiPCode = String(inmobiCfg.pCode);
    if (inmobiCfg.packageId) parsed.LevelPlayInMobiPackageId = String(inmobiCfg.packageId);
  } else {
    delete parsed.LevelPlayInMobiPCode;
    delete parsed.LevelPlayInMobiPackageId;
  }
  notes.push(`set LevelPlayCMPProvider=${useInMobi ? 'inmobi' : 'custom'}`);

  if (notes.length > 0) {
    fs.writeFileSync(plistPath, plistLib.build(parsed), 'utf8');
    console.log(`${TAG} ✅ Info.plist: ${notes.join(', ')}`);
  } else {
    console.log(`${TAG} ✓ Info.plist already up to date`);
  }
} else {
  console.log(`${TAG} ℹ️  iOS platform not found. Skipping Info.plist injection.`);
}

// ==========================================
// 4. ANDROID — CMP config string resources
// ==========================================
const resDir = path.join(projectRoot, 'android', 'app', 'src', 'main', 'res', 'values');
if (runAndroid && fs.existsSync(path.join(projectRoot, 'android', 'app'))) {
  if (!fs.existsSync(resDir)) fs.mkdirSync(resDir, { recursive: true });
  const xmlPath = path.join(resDir, 'levelplay_cmp.xml');
  const entries = [
    `    <string name="levelplay_cmp_provider" translatable="false">${useInMobi ? 'inmobi' : 'custom'}</string>`,
  ];
  if (useInMobi) {
    if (inmobiCfg.pCode) {
      entries.push(`    <string name="levelplay_inmobi_pcode" translatable="false">${escapeXml(inmobiCfg.pCode)}</string>`);
    }
    if (inmobiCfg.packageId) {
      entries.push(`    <string name="levelplay_inmobi_package_id" translatable="false">${escapeXml(inmobiCfg.packageId)}</string>`);
    }
  }
  const xml =
    `<?xml version="1.0" encoding="utf-8"?>\n` +
    `<!-- Generated by levelplay-manifest.js. Do not edit by hand. -->\n` +
    `<resources>\n${entries.join('\n')}\n</resources>\n`;
  fs.writeFileSync(xmlPath, xml, 'utf8');
  console.log(`${TAG} ✅ Wrote ${xmlPath} (CMP provider: ${useInMobi ? 'inmobi' : 'custom'})`);
}

function escapeXml(s) {
  return String(s).replace(/[<>&"']/g, (c) => ({
    '<': '&lt;', '>': '&gt;', '&': '&amp;', '"': '&quot;', "'": '&apos;',
  }[c]));
}

const summary = [];
if (networks.length) summary.push(`networks: ${networks.map((n) => n.key).join(', ')}`);
summary.push(`cmp: ${useInMobi ? 'inmobi' : 'custom'}`);
console.log(`${TAG} Done. ${summary.join(' | ')}`);
