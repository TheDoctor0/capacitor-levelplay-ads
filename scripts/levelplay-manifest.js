/**
 * LevelPlay mediation manifest injector.
 *
 * Run from the consuming Capacitor app. Reads the opt-in `levelplay` config
 * from the app's package.json and wires the selected mediation network
 * adapters into the native projects:
 *
 *   "levelplay": {
 *     "networks": ["admob", "applovin", "unityads"],
 *     "userTrackingDescription": "Your ads experience is improved with this."
 *   }
 *
 * For each network it injects:
 *   - Android: adapter `implementation` lines into android/app/build.gradle
 *   - iOS:     adapter pods into ios/App/Podfile
 *   - iOS:     NSUserTrackingUsageDescription + per-network SKAdNetwork IDs
 *              into Info.plist
 *
 * Injected regions are delimited by marker comments so re-runs replace them
 * cleanly rather than duplicating.
 */
const fs = require('fs');
const path = require('path');
const plistLib = require('plist');

const TAG = '[LevelPlay]';
const BEGIN = 'LEVELPLAY-ADAPTERS:BEGIN (generated by levelplay-manifest.js)';
const END = 'LEVELPLAY-ADAPTERS:END';
const REPOS_BEGIN = 'LEVELPLAY-REPOS:BEGIN (generated by levelplay-manifest.js)';
const REPOS_END = 'LEVELPLAY-REPOS:END';

/**
 * Per-network adapter coordinates — loaded from network-registry.json.
 * The IronSource SDK itself is bundled by the plugin; these are the
 * *mediation adapters* that bridge each demand source.
 */
const NETWORK_REGISTRY = require('./network-registry.json');

// Capacitor's `sync` fires `capacitor:sync:after` once per platform. Gate so
// each invocation only touches files for its own platform. When unset (manual
// run), apply everything.
const platform = (process.env.CAPACITOR_PLATFORM_NAME || '').toLowerCase();
if (platform === 'web') {
  process.exit(0);
}
const runAndroid = !platform || platform === 'android';
const runIos = !platform || platform === 'ios';

const projectRoot = process.cwd();
const packageJsonPath = path.join(projectRoot, 'package.json');

if (!fs.existsSync(packageJsonPath)) {
  console.log(`${TAG} package.json not found. Skipping mediation injection.`);
  process.exit(0);
}

const packageJson = JSON.parse(fs.readFileSync(packageJsonPath, 'utf8'));
const config = packageJson.levelplay || {};
const requested = Array.isArray(config.networks) ? config.networks : [];

// Consent provider — 'usercentrics' (default, IAB TCF v2.3 compliant),
// 'inmobi' (IAB TCF v2.2), or 'custom' (plugin's built-in modal, non-TCF).
// Backward compat: if consentProvider absent but inmobi.pCode set → 'inmobi'.
const inmobiCfg = config.inmobi || {};
const usercentricsCfg = config.usercentrics || {};
const admobCfg = config.admob || {};
const consentProvider = (config.consentProvider || 'usercentrics').toLowerCase();
if (!['usercentrics', 'inmobi', 'custom'].includes(consentProvider)) {
  console.log(`${TAG} ⚠️  Unknown consentProvider "${config.consentProvider}". Falling back to "usercentrics".`);
}
const useUsercentrics = consentProvider === 'usercentrics';
const useInMobi = consentProvider === 'inmobi';
if (useInMobi && !inmobiCfg.pCode) {
  console.log(
    `${TAG} ⚠️  consentProvider="inmobi" but no levelplay.inmobi.pCode in package.json. ` +
    `The CMP will not initialize. Get a pCode at https://choice.inmobi.com/.`,
  );
}
if (useUsercentrics && !usercentricsCfg.settingsId) {
  console.log(
    `${TAG} ⚠️  consentProvider="usercentrics" but no levelplay.usercentrics.settingsId in package.json. ` +
    `The CMP will not initialize. Get a settingsId at https://usercentrics.com/.`,
  );
}

const networks = [];
for (const name of requested) {
  const key = String(name).toLowerCase();
  if (NETWORK_REGISTRY[key]) {
    networks.push({ key, ...NETWORK_REGISTRY[key] });
  } else {
    console.log(`${TAG} ⚠️  Unknown network "${name}". Supported: ${Object.keys(NETWORK_REGISTRY).join(', ')}`);
  }
}

const useAdMob = networks.some((n) => n.key === 'admob');
if (useAdMob && !admobCfg.appId) {
  console.log(
    `${TAG} ⚠️  AdMob network enabled but no levelplay.admob.appId in package.json. ` +
    `The app WILL CRASH at launch without it. Get your App ID at https://admob.google.com/.`,
  );
}

if (networks.length === 0 && !useInMobi && !useUsercentrics) {
  console.log(`${TAG} No networks and no CMP to wire. Nothing to inject.`);
  process.exit(0);
}

// InMobi POM declares zero transitive deps. Full list per official guide:
// https://support.inmobi.com/choice/getting-started-cmp/implementing-cmp-via-code/mobile-app/android-app-implementation-sdk
const INMOBI_ANDROID_COORDS = [
  'com.inmobi:inmobicmp:2.4.2',
  'com.google.android.material:material:1.12.0',
  'com.google.code.gson:gson:2.10.1',
  'com.iabgpp:iabgpp-encoder:3.2.3',
  'androidx.preference:preference:1.2.1',
  'androidx.constraintlayout:constraintlayout:2.1.4',
  'androidx.recyclerview:recyclerview:1.3.2',
  'androidx.cardview:cardview:1.0.0',
  'androidx.lifecycle:lifecycle-runtime-ktx:2.8.7',
];
const USERCENTRICS_ANDROID_COORDS = [
  'com.usercentrics.sdk:usercentrics-ui:2.26.3',
];

/** Replaces the region between BEGIN/END markers, or returns null if absent. */
function replaceMarkedRegion(content, beginComment, endComment, body) {
  const re = new RegExp(`${escapeRe(beginComment)}[\\s\\S]*?${escapeRe(endComment)}`);
  if (re.test(content)) {
    return content.replace(re, body);
  }
  return null;
}

function escapeRe(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

// ==========================================
// 1. ANDROID — android/app/build.gradle (adapters) + android/build.gradle (repos)
// ==========================================
const gradlePath = path.join(projectRoot, 'android', 'app', 'build.gradle');
const rootGradlePath = path.join(projectRoot, 'android', 'build.gradle');
if (!runAndroid) {
  // skip — different platform pass
} else if (fs.existsSync(gradlePath)) {
  let gradle = fs.readFileSync(gradlePath, 'utf8');

  // Collect extra Maven repos needed by certain networks.
  // These go into the ROOT build.gradle (allprojects { repositories {} })
  // because app/build.gradle typically has no dependency repositories block.
  const repoUrls = new Set();
  networks.forEach((n) => {
    if (Array.isArray(n.androidRepos)) n.androidRepos.forEach((u) => repoUrls.add(u));
  });

  if (repoUrls.size > 0 && fs.existsSync(rootGradlePath)) {
    let rootGradle = fs.readFileSync(rootGradlePath, 'utf8');
    const repoLines = [...repoUrls].map((u) => `        maven { url '${u}' }`).join('\n');
    const reposBlock = `        // ${REPOS_BEGIN}\n${repoLines}\n        // ${REPOS_END}`;

    const reposReplaced = replaceMarkedRegion(rootGradle, `// ${REPOS_BEGIN}`, `// ${REPOS_END}`, reposBlock.trim());
    if (reposReplaced !== null) {
      rootGradle = reposReplaced;
      console.log(`${TAG} ✅ Updated extra Maven repos in android/build.gradle`);
    } else {
      const allProjReposRe = /(allprojects\s*\{[\s\S]*?repositories\s*\{)/;
      const rootReposRe = /(repositories\s*\{)/;
      const reposTarget = allProjReposRe.test(rootGradle) ? allProjReposRe : rootReposRe;
      if (reposTarget.test(rootGradle)) {
        rootGradle = rootGradle.replace(reposTarget, `$1\n${reposBlock}`);
        console.log(`${TAG} ✅ Injected extra Maven repos into android/build.gradle`);
      } else {
        console.log(`${TAG} ⚠️  Could not find repositories block in android/build.gradle. Add these Maven repos manually:\n${[...repoUrls].join('\n')}`);
      }
    }
    fs.writeFileSync(rootGradlePath, rootGradle, 'utf8');
  } else if (repoUrls.size === 0 && fs.existsSync(rootGradlePath)) {
    let rootGradle = fs.readFileSync(rootGradlePath, 'utf8');
    const reposReplaced = replaceMarkedRegion(rootGradle, `// ${REPOS_BEGIN}`, `// ${REPOS_END}`, '');
    if (reposReplaced !== null) {
      rootGradle = reposReplaced;
      fs.writeFileSync(rootGradlePath, rootGradle, 'utf8');
      console.log(`${TAG} ✅ Removed stale extra Maven repos from android/build.gradle`);
    }
  }

  const coords = [];
  networks.forEach((n) => {
    coords.push(n.android);
    if (Array.isArray(n.androidSdks)) coords.push(...n.androidSdks);
  });
  if (useInMobi) coords.push(...INMOBI_ANDROID_COORDS);
  if (useUsercentrics) coords.push(...USERCENTRICS_ANDROID_COORDS);
  const lines = coords.map((c) => `    implementation '${c}'`).join('\n');
  const block = `// ${BEGIN}\ndependencies {\n${lines}\n}\n// ${END}`;

  const replaced = replaceMarkedRegion(gradle, `// ${BEGIN}`, `// ${END}`, block);
  if (replaced !== null) {
    gradle = replaced;
    console.log(`${TAG} ✅ Updated Android mediation adapters in android/app/build.gradle`);
  } else {
    gradle = `${gradle.trimEnd()}\n\n${block}\n`;
    console.log(`${TAG} ✅ Injected Android mediation adapters into android/app/build.gradle`);
  }
  // Clean up any stale repos previously injected into app/build.gradle (moved to root).
  const staleRepos = replaceMarkedRegion(gradle, `// ${REPOS_BEGIN}`, `// ${REPOS_END}`, '');
  if (staleRepos !== null) gradle = staleRepos;

  fs.writeFileSync(gradlePath, gradle, 'utf8');
} else if (fs.existsSync(`${gradlePath}.kts`)) {
  // Kotlin-DSL build scripts use a different dependency syntax — don't risk a
  // malformed inject; tell the developer what to add by hand.
  const ktsCoords = [];
  networks.forEach((n) => {
    ktsCoords.push(n.android);
    if (Array.isArray(n.androidSdks)) ktsCoords.push(...n.androidSdks);
  });
  if (useInMobi) ktsCoords.push(...INMOBI_ANDROID_COORDS);
  if (useUsercentrics) ktsCoords.push(...USERCENTRICS_ANDROID_COORDS);
  const ktsLines = ktsCoords.map((c) => `    implementation("${c}")`).join('\n');
  const ktsRepoUrls = new Set();
  networks.forEach((n) => {
    if (Array.isArray(n.androidRepos)) n.androidRepos.forEach((u) => ktsRepoUrls.add(u));
  });
  let ktsMsg = `${TAG} ⚠️  Found build.gradle.kts (Kotlin DSL). Add these to the dependencies block manually:\n${ktsLines}`;
  if (ktsRepoUrls.size > 0) {
    const ktsRepoLines = [...ktsRepoUrls].map((u) => `    maven("${u}")`).join('\n');
    ktsMsg += `\n\nAlso add these Maven repos to your repositories block:\n${ktsRepoLines}`;
  }
  console.log(ktsMsg);
} else {
  console.log(`${TAG} ℹ️  Android platform not found. Skipping Android adapter injection.`);
}

// ==========================================
// 1b. ANDROID — settings.gradle (extra Maven repos for dependencyResolutionManagement)
// ==========================================
const settingsPath = path.join(projectRoot, 'android', 'settings.gradle');
const settingsKtsPath = path.join(projectRoot, 'android', 'settings.gradle.kts');
if (runAndroid) {
  const repoUrls = new Set();
  networks.forEach((n) => {
    if (Array.isArray(n.androidRepos)) n.androidRepos.forEach((u) => repoUrls.add(u));
  });
  if (repoUrls.size > 0) {
    const sPath = fs.existsSync(settingsKtsPath) ? settingsKtsPath : (fs.existsSync(settingsPath) ? settingsPath : null);
    if (sPath) {
      let settings = fs.readFileSync(sPath, 'utf8');
      if (settings.includes('dependencyResolutionManagement')) {
        const isKts = sPath.endsWith('.kts');
        const repoLines = [...repoUrls].map((u) =>
          isKts ? `        maven("${u}")` : `        maven { url '${u}' }`,
        ).join('\n');
        const sReposBlock = `        // ${REPOS_BEGIN}\n${repoLines}\n        // ${REPOS_END}`;

        const sReplaced = replaceMarkedRegion(settings, `// ${REPOS_BEGIN}`, `// ${REPOS_END}`, sReposBlock.trim());
        if (sReplaced !== null) {
          settings = sReplaced;
          console.log(`${TAG} ✅ Updated extra Maven repos in ${path.basename(sPath)}`);
        } else {
          const drmReposRe = /(dependencyResolutionManagement\s*\{[\s\S]*?repositories\s*\{)/;
          if (drmReposRe.test(settings)) {
            settings = settings.replace(drmReposRe, `$1\n${sReposBlock}`);
            console.log(`${TAG} ✅ Injected extra Maven repos into ${path.basename(sPath)}`);
          } else {
            console.log(`${TAG} ⚠️  Could not find repositories block in ${path.basename(sPath)}. Add these Maven repos manually:\n${[...repoUrls].join('\n')}`);
          }
        }
        fs.writeFileSync(sPath, settings, 'utf8');
      }
    }
  }
}

// ==========================================
// 2. IOS — ios/App/Podfile
// ==========================================
const podfilePath = path.join(projectRoot, 'ios', 'App', 'Podfile');
if (!runIos) {
  // skip — different platform pass
} else if (fs.existsSync(podfilePath)) {
  let podfile = fs.readFileSync(podfilePath, 'utf8');

  const podCoords = networks.map((n) => `  pod '${n.ios}'`);
  const pods = podCoords.join('\n');
  const block = `  # ${BEGIN}\n${pods}\n  # ${END}`;

  const replaced = replaceMarkedRegion(podfile, `# ${BEGIN}`, `# ${END}`, block.trim());
  if (replaced !== null) {
    podfile = replaced;
    console.log(`${TAG} ✅ Updated iOS mediation adapter pods in Podfile`);
  } else {
    // Insert inside the `target 'App' do` block, before its `end`.
    const targetRe = /(target ['"]App['"] do[\s\S]*?)(\n\s*end)/;
    if (targetRe.test(podfile)) {
      podfile = podfile.replace(targetRe, `$1\n${block}$2`);
      console.log(`${TAG} ✅ Injected iOS mediation adapter pods into Podfile`);
    } else {
      console.log(`${TAG} ⚠️  Could not locate the "App" target in Podfile. Add these pods manually:\n${pods}`);
    }
  }

  // Rewrite the plugin's pod line to include the selected CMP subspec.
  // Capacitor auto-generates: pod 'CapacitorLevelplayAds', :path => '...'
  // We append :subspecs => ['Core', '<CMP>'] so only the chosen SDK compiles in.
  const cmpSubspec = useUsercentrics ? 'Usercentrics' : useInMobi ? 'InMobi' : null;
  const pluginPodRe = /(pod\s+['"]CapacitorLevelplayAds['"],\s*:path\s*=>\s*['"][^'"]+['"])([^\n]*)/;
  if (pluginPodRe.test(podfile)) {
    if (cmpSubspec) {
      podfile = podfile.replace(pluginPodRe, `$1, :subspecs => ['Core', '${cmpSubspec}']`);
      console.log(`${TAG} ✅ Podfile: CapacitorLevelplayAds → subspecs Core + ${cmpSubspec}`);
    } else {
      // custom mode — Core only, no CMP SDK
      podfile = podfile.replace(pluginPodRe, `$1`);
      console.log(`${TAG} ✅ Podfile: CapacitorLevelplayAds → Core only (no CMP)`);
    }
  } else {
    console.log(`${TAG} ⚠️  Could not find CapacitorLevelplayAds pod line in Podfile. Add subspecs manually.`);
  }
  // IronSourceSDK ships as a static xcframework. Capacitor's default
  // `use_frameworks!` (dynamic) rejects transitive static binaries.
  // Patch to `:linkage => :static` if not already set.
  if (/use_frameworks!\s*$/.test(podfile) && !/use_frameworks!\s+:linkage\s*=>/.test(podfile)) {
    podfile = podfile.replace(/use_frameworks!\s*$/m, "use_frameworks! :linkage => :static");
    console.log(`${TAG} ✅ Patched Podfile: use_frameworks! → use_frameworks! :linkage => :static`);
  }

  fs.writeFileSync(podfilePath, podfile, 'utf8');
} else {
  console.log(`${TAG} ℹ️  iOS platform not found. Skipping Podfile injection.`);
}

// ==========================================
// 3. IOS — Info.plist (ATT description + SKAdNetwork IDs)
// ==========================================
const plistPath = path.join(projectRoot, 'ios', 'App', 'App', 'Info.plist');
if (!runIos) {
  // skip — different platform pass
} else if (fs.existsSync(plistPath)) {
  const raw = fs.readFileSync(plistPath, 'utf8');
  const parsed = plistLib.parse(raw);
  const notes = [];

  // 3a. App Tracking Transparency usage description — insert only when
  // missing. The wording often goes through legal/localization review;
  // never overwrite it.
  if (!parsed.NSUserTrackingUsageDescription) {
    parsed.NSUserTrackingUsageDescription =
      config.userTrackingDescription ||
      'This identifier will be used to deliver personalized ads to you.';
    notes.push('added NSUserTrackingUsageDescription');
  }

  // 3b. SKAdNetwork IDs — merge with whatever is already there. Other SDKs
  // (AdMob, Firebase, RevenueCat) register their own IDs in the same array;
  // a replace would silently strip them.
  const ourIds = new Set(['su67r6k2v3.skadnetwork']); // IronSource
  networks.forEach((n) => n.skAdNetworkIds.forEach((id) => ourIds.add(id)));

  const items = Array.isArray(parsed.SKAdNetworkItems) ? parsed.SKAdNetworkItems : [];
  const existing = new Set(items.map((i) => i?.SKAdNetworkIdentifier).filter(Boolean));
  const missing = [...ourIds].filter((id) => !existing.has(id));
  if (missing.length > 0) {
    parsed.SKAdNetworkItems = [...items, ...missing.map((id) => ({ SKAdNetworkIdentifier: id }))];
    notes.push(`added ${missing.length} SKAdNetwork ID${missing.length === 1 ? '' : 's'}`);
  }

  // 3c. AdMob app ID — MANDATORY, app crashes at launch without it.
  if (useAdMob && admobCfg.appId) {
    parsed.GADApplicationIdentifier = String(admobCfg.appId);
    notes.push('set GADApplicationIdentifier');
  } else if (!useAdMob) {
    delete parsed.GADApplicationIdentifier;
  }

  // 3d. Vungle requires status bar control for fullscreen ads.
  const useVungle = networks.some((n) => n.key === 'vungle');
  if (useVungle && parsed.UIViewControllerBasedStatusBarAppearance !== false) {
    parsed.UIViewControllerBasedStatusBarAppearance = false;
    notes.push('set UIViewControllerBasedStatusBarAppearance=NO (Vungle)');
  }

  // 3e. CMP provider selection — plugin reads these at runtime to decide
  // which ConsentProvider to instantiate.
  parsed.LevelPlayCMPProvider = consentProvider;
  if (useInMobi) {
    if (inmobiCfg.pCode) parsed.LevelPlayInMobiPCode = String(inmobiCfg.pCode);
    if (inmobiCfg.packageId) parsed.LevelPlayInMobiPackageId = String(inmobiCfg.packageId);
  } else {
    delete parsed.LevelPlayInMobiPCode;
    delete parsed.LevelPlayInMobiPackageId;
  }
  if (useUsercentrics) {
    if (usercentricsCfg.settingsId) parsed.LevelPlayUsercentricsSettingsId = String(usercentricsCfg.settingsId);
    if (usercentricsCfg.rulesetId) parsed.LevelPlayUsercentricsRulesetId = String(usercentricsCfg.rulesetId);
  } else {
    delete parsed.LevelPlayUsercentricsSettingsId;
    delete parsed.LevelPlayUsercentricsRulesetId;
  }
  notes.push(`set LevelPlayCMPProvider=${consentProvider}`);

  if (notes.length > 0) {
    fs.writeFileSync(plistPath, plistLib.build(parsed), 'utf8');
    console.log(`${TAG} ✅ Info.plist: ${notes.join(', ')}`);
  } else {
    console.log(`${TAG} ✓ Info.plist already up to date`);
  }
} else {
  console.log(`${TAG} ℹ️  iOS platform not found. Skipping Info.plist injection.`);
}

// ==========================================
// 4. ANDROID — CMP config string resources
// ==========================================
const resDir = path.join(projectRoot, 'android', 'app', 'src', 'main', 'res', 'values');
if (runAndroid && fs.existsSync(path.join(projectRoot, 'android', 'app'))) {
  if (!fs.existsSync(resDir)) fs.mkdirSync(resDir, { recursive: true });
  const xmlPath = path.join(resDir, 'levelplay_cmp.xml');
  const entries = [
    `    <string name="levelplay_cmp_provider" translatable="false">${consentProvider}</string>`,
  ];
  if (useInMobi) {
    if (inmobiCfg.pCode) {
      entries.push(`    <string name="levelplay_inmobi_pcode" translatable="false">${escapeXml(inmobiCfg.pCode)}</string>`);
    }
    if (inmobiCfg.packageId) {
      entries.push(`    <string name="levelplay_inmobi_package_id" translatable="false">${escapeXml(inmobiCfg.packageId)}</string>`);
    }
  }
  if (useUsercentrics) {
    if (usercentricsCfg.settingsId) {
      entries.push(`    <string name="levelplay_usercentrics_settings_id" translatable="false">${escapeXml(usercentricsCfg.settingsId)}</string>`);
    }
    if (usercentricsCfg.rulesetId) {
      entries.push(`    <string name="levelplay_usercentrics_ruleset_id" translatable="false">${escapeXml(usercentricsCfg.rulesetId)}</string>`);
    }
  }
  const xml =
    `<?xml version="1.0" encoding="utf-8"?>\n` +
    `<!-- Generated by levelplay-manifest.js. Do not edit by hand. -->\n` +
    `<resources>\n${entries.join('\n')}\n</resources>\n`;
  fs.writeFileSync(xmlPath, xml, 'utf8');
  console.log(`${TAG} ✅ Wrote ${xmlPath} (CMP provider: ${consentProvider})`);

  // AdMob APPLICATION_ID meta-data — MANDATORY, crashes at launch if missing.
  if (useAdMob && admobCfg.appId) {
    const manifestPath = path.join(projectRoot, 'android', 'app', 'src', 'main', 'AndroidManifest.xml');
    if (fs.existsSync(manifestPath)) {
      let manifest = fs.readFileSync(manifestPath, 'utf8');
      const admobMetaTag = `<meta-data android:name="com.google.android.gms.ads.APPLICATION_ID" android:value="${escapeXml(admobCfg.appId)}"/>`;
      if (manifest.includes('com.google.android.gms.ads.APPLICATION_ID')) {
        manifest = manifest.replace(
          /<meta-data\s+android:name="com\.google\.android\.gms\.ads\.APPLICATION_ID"\s+android:value="[^"]*"\s*\/>/,
          admobMetaTag,
        );
        console.log(`${TAG} ✅ Updated AdMob APPLICATION_ID in AndroidManifest.xml`);
      } else if (manifest.includes('<application')) {
        manifest = manifest.replace(
          /(<application[^>]*>)/,
          `$1\n        ${admobMetaTag}`,
        );
        console.log(`${TAG} ✅ Injected AdMob APPLICATION_ID into AndroidManifest.xml`);
      } else {
        console.log(`${TAG} ⚠️  Could not find <application> tag. Add AdMob APPLICATION_ID meta-data manually.`);
      }
      fs.writeFileSync(manifestPath, manifest, 'utf8');
    }
  }
}

function escapeXml(s) {
  return String(s).replace(/[<>&"']/g, (c) => ({
    '<': '&lt;', '>': '&gt;', '&': '&amp;', '"': '&quot;', "'": '&apos;',
  }[c]));
}

// ==========================================
// 5. ANDROID — network_security_config.xml (Meta / Smaato)
// ==========================================
const useMeta = networks.some((n) => n.key === 'meta');
const useSmaato = networks.some((n) => n.key === 'smaato');
const xmlResDir = path.join(projectRoot, 'android', 'app', 'src', 'main', 'res', 'xml');
if (runAndroid && (useMeta || useSmaato) && fs.existsSync(path.join(projectRoot, 'android', 'app'))) {
  if (!fs.existsSync(xmlResDir)) fs.mkdirSync(xmlResDir, { recursive: true });
  const nscPath = path.join(xmlResDir, 'network_security_config.xml');
  if (!fs.existsSync(nscPath)) {
    // Smaato needs full cleartext; Meta only needs localhost.
    // Use the more permissive config when Smaato is present.
    const nsc = useSmaato
      ? `<?xml version="1.0" encoding="utf-8"?>\n` +
        `<!-- Required by Smaato SDK (cleartext traffic). -->\n` +
        `<network-security-config>\n` +
        `    <base-config cleartextTrafficPermitted="true" />\n` +
        `</network-security-config>\n`
      : `<?xml version="1.0" encoding="utf-8"?>\n` +
        `<!-- Required by Meta Audience Network SDK for cleartext to localhost. -->\n` +
        `<network-security-config>\n` +
        `    <base-config cleartextTrafficPermitted="false" />\n` +
        `    <domain-config cleartextTrafficPermitted="true">\n` +
        `        <domain includeSubdomains="true">127.0.0.1</domain>\n` +
        `    </domain-config>\n` +
        `</network-security-config>\n`;
    fs.writeFileSync(nscPath, nsc, 'utf8');
    const reason = [useMeta && 'Meta', useSmaato && 'Smaato'].filter(Boolean).join(' + ');
    console.log(
      `${TAG} ✅ Created network_security_config.xml (${reason}).\n` +
      `${TAG} ⚠️  Add android:networkSecurityConfig="@xml/network_security_config" to your <application> tag in AndroidManifest.xml if not already present.`,
    );
  }
}

